defmodule Util.Server do 
    @moduledoc """
    """
    
    ##==========================================================================
    ## Module require
    ##==========================================================================
    require Logger
    
    ##==========================================================================
    ## Module uses
    ##==========================================================================
    use GenServer
    
    ##==========================================================================
    @doc """
    """
    def start_link() do
        GenServer.start_link(   __MODULE__, 
                                [], 
                                [name: {:global, :printer}])
    end

    ##==========================================================================
    @doc """
    """
    def init([]) do
        {:ok, :ok}
    end

    ##==========================================================================
    ## GenServer API 
    ##==========================================================================

    ##==========================================================================
    @doc """
    """
    def handle_call(_msg, _from, state) do
        {:reply, :ok, state}
    end
    
    ##==========================================================================
    @doc """
    """
    def handle_cast({:print, text}, state) do
        Logger.debug(text)
        {:noreply, state}
    end

    def handle_cast(_msg, state) do
        {:noreply, state}
    end

    ##==========================================================================
    @doc """
    """
    def handle_info(_msg, state) do 
        {:noreply, state}
    end

    ##==========================================================================
    @doc """
    """
    def terminate(_reason, _state) do
        :ok
    end

end
